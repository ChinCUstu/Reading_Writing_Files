import turtle


def square():
    turtle.setheading(45)
    turtle.forward(75)
    turtle.setheading(315)
    turtle.forward(75)
    turtle.right(90)
    turtle.forward(75)
    turtle.right(90)
    turtle.forward(75)


def half_square():
    turtle.setheading(45)
    turtle.forward(75)
    turtle.setheading(315)
    turtle.forward(75)
